#include <iostream>

int main(){
    using namespace std;

    int a = 10;
    int b = 3;

    cout << "a + b = "<< a+b << endl;
    cout << "a - b = "<< a-b << endl;
    cout << "a * b = "<< a*b << endl;
    cout << "a / b = "<< a/b << endl;
    cout << "a % b = "<< a%b << endl;

    return 0;
}